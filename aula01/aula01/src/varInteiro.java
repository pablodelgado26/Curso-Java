public class varInteiro {

    public static void main(String[] args) throws Exception {
        // Declarando uma vari√°vel do tipo inteiro

        // byte pode armazenar valores inteiros de -128 a 127

        byte byteVar = 127;
        System.out.println("O valor e: " + byteVar);

        // short pode armazenar valores inteiros de -32,768 a 32,767

        short shortVar = 32767;
        System.out.println("O valor e: " + shortVar);

        // int pode armazenar valores inteiros de -2,147,483,648 a 2,147,483,647

        int intVar = 2147483647;
        System.out.println("O valor e: " + intVar);
    }
}