public class varCaractere {
    public static void main(String[] args) throws Exception {
        // Declarando uma variável do tipo caractere

        // O tipo char é um tipo de dado primitivo que representa um único caractere Unicode. Ele é de 16 bits e pode representar qualquer caractere Unicode, como letras, números, sinais de pontuação e outros símbolos. sempre é declarado entre aspas simples.
        
        char caractere = 'A';
        System.out.println("O caractere e: " + caractere);

        // O tipo string é um tipo de dado que representa uma sequência de caracteres. Ele é uma classe e não um tipo de dado primitivo. Uma string é uma sequência de caracteres entre aspas duplas.

        String texto = "Ola, mundo!";
        System.out.println("O texto e: " + texto);

        // O tipo boolean é um tipo de dado primitivo que representa um valor lógico. Ele pode ser verdadeiro ou falso. O tipo boolean é usado em expressões lógicas e em estruturas de controle de fluxo, como condicionais e loops.

        boolean verdadeiro = true;
        System.out.println("O valor e: " + verdadeiro);

        boolean falso = false;
        System.out.println("O valor e: " + falso);
    }
}
