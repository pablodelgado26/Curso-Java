public class varPontoFlutuante {
    public static void main(String[] args) throws Exception {
        // Declarando uma variável do tipo ponto flutuante

        // float é um tipo de dado primitivo que representa um número de ponto flutuante de precisão simples. Ele é de 32 bits e pode representar números decimais com até 7 dígitos de precisão.
        
        float floatVar = 3.14159f;
        System.out.println("O valor e: " + floatVar);

        // double é um tipo de dado primitivo que representa um número de ponto flutuante de precisão dupla. Ele é de 64 bits e pode representar números decimais com até 15 dígitos de precisão.

        double doubleVar = 3.14159265359;
        System.out.println("O valor e: " + doubleVar);
        
    }
}
